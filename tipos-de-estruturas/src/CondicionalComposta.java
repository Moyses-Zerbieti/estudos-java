public class  CondicionalComposta {
    public static void main(String[] args) {
        int nota = 8;

        if (nota >= 7){
            System.out.println("Aprovado");

        }else{
            System.out.println("Reprovado");
        }
    }
}//Condicional composta utilizada verifica uma de duas condições para dar continuidade no código
//No caso aqui o código da continuidade quando apenas UMA das duas condições (if "SE") e (else "SE NÃO")forem verdadeiras
//Se as duas forem verdade ou falsas o código irá retornar um ERRO 


